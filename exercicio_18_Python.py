import math
angulo = int(input('Digite o angulo desejado: '))
print(f'O angulo de {angulo} tem o SENO de {math.sin(math.radians(angulo)):.2f}')
print(f'O angulo de {angulo} tem o COSENO de {math.cos(math.radians(angulo)):.2f}')
print(f'O angulo de {angulo} tem o TANGENTE de {math.tan(math.radians(angulo)):.2f}')